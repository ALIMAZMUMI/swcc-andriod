package com.gov.sa.swcc.model;

public class LeaveItems {

    String Pernr;
    String VacationCode;
    String Begda;
    String Endda;
    String NoDays;

    public String getPernr() {
        return Pernr;
    }

    public void setPernr(String pernr) {
        Pernr = pernr;
    }

    public String getVacationCode() {
        return VacationCode;
    }

    public void setVacationCode(String vacationCode) {
        VacationCode = vacationCode;
    }

    public String getBegda() {
        return Begda;
    }

    public void setBegda(String begda) {
        Begda = begda;
    }

    public String getEndda() {
        return Endda;
    }

    public void setEndda(String endda) {
        Endda = endda;
    }

    public String getNoDays() {
        return NoDays;
    }

    public void setNoDays(String noDays) {
        NoDays = noDays;
    }
}
